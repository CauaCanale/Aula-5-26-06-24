// 7- Escreva um programa que leia dois números e uma operação (adição, subtração, multiplicação, divisão) e realize a operação entre os dois números.

numero_escolhido1 = 254
numero_escolhido2 = 173
operacao = '+'

if (operacao == '+'){
    console.log('O valor da soma é', numero_escolhido1 + numero_escolhido2)
    
}
else if (operacao == '-'){
    console.log('o valor da subtracao é', numero_escolhido1 - numero_escolhido2)
    
}
else if (operacao == '/'){
    console.log('o valor da divisao é', numero_escolhido1 / numero_escolhido2)
    
}
else if (operacao == '*'){
    console.log('o valor da multiplicacao é', numero_escolhido1 * numero_escolhido2)
    
}

/*Explicação de cada Sinal.

* Atribuição   | =
* Adição       | +
* Subtração    | -
* Divisão      | /
* Multiplicação| *
* Negação      | !
* Igual        | ==
* Diferente    | !=
* Maior        | >
* Menor        | <
* Maior igual  | >=
* Menor igual  | <=
*/